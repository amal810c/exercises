"use strict";

//#c0ffee
hexToRGB("#c0ffee");

function hexToRGB(string) {
  //split hex into tree parts and convert to numbers
  const r = parseInt(string.substring(1, 3), 16);
  const g = parseInt(string.substring(3, 5), 16);
  const b = parseInt(string.substring(5, 7), 16);
  console.log(`${r} ${g} ${b}`);
}

